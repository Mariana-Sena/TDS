
public class Departamento {
	
	public String nome;
	public int bloco;
	
	public Departamento() {
		this.nome = "";
		this.bloco = 0;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getBloco() {
		return bloco;
	}

	public void setBloco(int bloco) {
		this.bloco = bloco;
	}
}
